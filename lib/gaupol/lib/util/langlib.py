def get_descriptive_name(code):
    """
    Get descriptive name "Language (code)" for language code.
    
    Return format and language name are translation dependent.
    Raise KeyError is language not found.
    """
    # TRANSLATORS: Language descriptive name, e.g. "English (en_US)".
    return _('%s (%s)') % (LANGS[code[:2]], code)
    
def get_language(code):
    """
    Get language based on language code.
    
    Only the first two letters of the language code will be considered, so
    giving e.g. "en_US" will return the same as giving "en".
    
    Raise KeyError is language not found.
    """
    return LANGS[code[:2]]
